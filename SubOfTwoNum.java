//Wap to find subtraction of two numbers
import java.util.*;
public class SubOfTwoNum {
    public static void main(String args[])
    {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter two numbers : ");
        int a = sc.nextInt();
        int b = sc.nextInt();
        int sub = a-b;
        System.out.println("Sub = "+ sub);
    }
    
}
