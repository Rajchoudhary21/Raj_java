//Wap to find multiplication of two numbers
import java.util.*;
public class MulOfTwoNum {
    public static void main(String args[])
    {
        Scanner sc = new Scanner(System.in);
        System.out.print("Enter two numbers : ");
        int a = sc.nextInt();
        int b = sc.nextInt();
        int mul = a*b;
        System.out.println( "Mul= "+ mul);
    }
    
}
